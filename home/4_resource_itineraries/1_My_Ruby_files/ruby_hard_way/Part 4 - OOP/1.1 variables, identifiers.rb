=begin 1.

There are 4 types of variables:
  1. Local variable - normal variables
  2. Gloal variables - have a leading $
  3. Instance variables (data memebers) - leading @
  4. Class variables (static) - leading @@

  Notes:
    - Naming conventions  - Just like C++/Python.
    - Global variable can be named anything - even numbers
=end

=begin 2. Other identifiers
   There are other identifiers:
   1. Constants - begin with a capital letter
   2. Keywords.
   3. Method names - can end with ?, ! or =
=end
